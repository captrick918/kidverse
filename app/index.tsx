export default function Index() {
  const [verseIndex, setVerseIndex] = useState(0);

  // nav helpers
  const prevVerse = () => {
    setVerseIndex(prev => (prev - 1 + verses.length) % verses.length);
  };

  const nextVerse = () => {
    setVerseIndex(prev => (prev + 1) % verses.length);
  };

  // show day number (1–365)
  const dayNumber = (verseIndex % 365) + 1;

  // ----- SWIPE SETUP -----
  const SWIPE_THRESHOLD = 40;

  const panResponder = useRef(
    PanResponder.create({
      // start handling if the user moves their finger horizontally
      onMoveShouldSetPanResponder: (_evt, gestureState) =>
        Math.abs(gestureState.dx) > 10,
      onMoveShouldSetPanResponderCapture: (_evt, gestureState) =>
        Math.abs(gestureState.dx) > 10,

      onPanResponderRelease: (_evt, gestureState) => {
        if (gestureState.dx > SWIPE_THRESHOLD) {
          // swipe right → previous verse
          prevVerse();
        } else if (gestureState.dx < -SWIPE_THRESHOLD) {
          // swipe left → next verse
          nextVerse();
        }
      },
    })
  ).current;

  // pick verse + background
  const verse = verses[verseIndex];
  const bgIndex = verseIndex % backgrounds.length;

  return (
    <ImageBackground
      source={backgrounds[bgIndex]}
      style={styles.background}
      resizeMode="cover"
      {...panResponder.panHandlers}
    >
      <FloatingShapes />

      <SafeAreaView style={styles.safe}>
        <StatusBar barStyle="light-content" />

        <View style={styles.centerWrapper}>
          {/* Day pill */}
          <View style={styles.dayPill}>
            <Text style={styles.dayText}>Day {dayNumber} of 365</Text>
          </View>

          {/* Verse card (centered) */}
          <View style={styles.card}>
            <Text style={styles.verseText}>{verse.text}</Text>
            <Text style={styles.reference}>{verse.reference}</Text>
          </View>

          {/* Buttons */}
          <View style={styles.buttons}>
            <View style={styles.row}>
              <TouchableOpacity style={styles.smallButton} onPress={prevVerse}>
                <Text style={styles.smallButtonText}>◀︎ Previous</Text>
              </TouchableOpacity>
              <TouchableOpacity style={styles.smallButton} onPress={nextVerse}>
                <Text style={styles.smallButtonText}>Next ▶︎</Text>
              </TouchableOpacity>
            </View>
          </View>
        </View>
      </SafeAreaView>
    </ImageBackground>
  );
}

